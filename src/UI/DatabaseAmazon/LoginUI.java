    private void jLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jLoginActionPerformed
        // TODO add your handling code here:
        String user = jUser.getText();
        char[] pwd = jPwd.getPassword();
        
        
        mysqlConnection = new CreateMysqlConnection(user, String.valueOf(pwd));
        //JOptionPane.showMessageDialog(this, user);
        _mysqlDB = mysqlConnection.openConnection();
        
        if (_mysqlDB != null) {
            //new BasicQueryUI(_mysqlDB).setVisible(true);
            jBasicButton.setEnabled(true);
            jAdvancedButton.setEnabled(true);
            jUpdateButton.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(this, "Cannot connect the database");
        }
        
    }//GEN-LAST:event_jLoginActionPerformed

